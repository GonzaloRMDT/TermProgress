namespace TermProgress.Library.Options
{
    /// <summary>
    /// Represents the Twitter client options.
    /// </summary>
    public class TwitterClientOptions
    {
        /// <summary>
        /// Gets or sets the client access token.
        /// </summary>
        public string AccessToken { get; set; }

        /// <summary>
        /// Gets or sets the client access token secret.
        /// </summary>
        public string AccessTokenSecret { get; set; }

        /// <summary>
        /// Gets or sets the client consumer key.
        /// </summary>
        public string ConsumerKey { get; set; }

        /// <summary>
        /// Gets or sets the client consumer key.
        /// </summary>
        public string ConsumerSecret { get; set; }
    }
}
